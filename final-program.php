<p class="legend">Keynote Lecture</p>
<h6 class="info">10:00 AM - 10:45 AM | Session Chair: Dr. Mojtaba Ahmadi, Carleton University</h6>
<h3 class="title">Formation Control of Autonomous Vehicles<br>
<small><a href="http://www.flight.utias.utoronto.ca/fsc/index.php?id=204" target="_blank">Dr. Hugh H.T. Liu, University of Toronto, Canada
</a></small></h3>
<br>

<dl class="dl-horizontal">
  <dt>Abstract</dt>
  <dd>Modern aircraft design has seen increases in aspect-ratio of wings and uses of composite materials to reduce airframe weight, which contribute to higher aerodynamic efficiency, less fuel consumption and thus longer endurance. It is often developed for high-altitude long-endurance (HALE) flight applications. While benefiting from such design, new challenges arise as these improvements also give rise to more structural flexibility in aircraft wings. The interaction between rigid-body dynamics, structural dynamics and aerodynamics may become strong enough to produce undesirable aero-elastic effects. Therefore, it is important to take into account the significant aero-elastic modes in modeling and control design for flexible aircraft more than ever when traditional aircraft may only have very limited aero-elastic effects. In this talk, recent research work will be highlighted in this area, focusing on a uniform dynamics modeling platform, an integrated control design that is capable of both rigid-body motion control and aero-elastic mode suppression. Further, this talk will present the latest work of addressing gust alleviation, as well as fault-tolerant design in the presence of faulted control surfaces as well as actuator/sensor failures. In addition, research work of formation flight of UAVs will be presented with successful field flight demonstrations.</dd>
</dl>
