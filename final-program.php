      <h4 class="panel-title">
        <a  class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapse135" aria-expanded="false" aria-controls="collapse135">Revised Propeller Dynamics and Energy-Optimal Hovering in a Monospinner<br></a>
          <small>Mojtaba Hedayatpour, University of Regina, Canada</small>
      </h4>
    </div>

    <div id="collapse135" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading135">
      <div class="panel-body">
        <dl class="dl-horizontal">
          <dt>Time</dt>
          <dd>1:05 - 1:25</dd>
          <dt>Authors</dt>
          <dd>Mojtaba Hedayatpour, Mehran Mehrandezh, Farrokh Janabi-Sharifi</dd>
           <dt>Abstract</dt>
          <dd>This paper presents modelling, control and simulation results of an unmanned aerial vehicle (UAV) with only one fixed motor spinning a tilted propeller called “Monospinner”. This class of rotary-wing UAVs is gaining some significant attention since they can lead to the design and development of control strategies for safe landing in case of one single or multiple rotor failure. Furthermore, the studies on monospinners can lead to the development of the most energy-efficient rotary wing UAVs with a potential to render themselves as high-endurance flying machines. In a monospinner, unlike most flying vehicles with rotary wings, hovering is defined as maintaining the altitude while rotating about an axis that is fixed with respect to the vehicle. Therefore, not all degrees of motion can be fully controlled. However, the remaining controllable states of the machine will be good enough to maintain a stable altitude, or even to track a trajectory. For this purpose, a linear time-invariant system is derived for controlling the attitude. It is shown that by controlling the attitude, the direction of that fixed axis of rotation (i.e., a precession axis) can be controlled by which the altitude of the vehicle can be kept at constant. A complete aerodynamic model of the propeller experiencing fast rotations about the precession axis is presented for the first time followed by analysing the effect a tilting rotor can have on the total power required to maintain a constant altitude. The proposed dynamic model and the design were evaluated via a nonlinear simulation system. </dd>
          <dt>Keywords</dt>
          <dd>Unmanned flying vehicles, minimum power flight, propeller, tilting rotors, monospinner, stable flight despite rotor failure.</dd>
        </dl>
