          <dd> 1:25 - 1:45</dd>
          <dt>Authors</dt>
          <dd>CassidyWestin, Rishad A. Irani</dd>
           <dt>Abstract</dt>
          <dd>This paper presents a finite element model of a flexible cable that has been constructed to accurately model dynamic wrap angles around a static pulley. The study considers a system with a rope running over a pulley and a suspended load on one end moving in a circular arc. The model utilizes the Absolute Nodal Coordinate Formulation to define the generalized coordinates, chosen for its accurate definitions of large non-linear cable deformations. The interaction of the cable with the surface of a pulley is modeled using a contact penalty formulation. An experimental study is performed to analyze the performance of the model outputs: cable tension and wrap angle. During the testing the wrap angle had peak-to-peak oscillations of up to 122 degrees. The numerical simulation shows reasonable agreement with the experimental measurements. </dd>
