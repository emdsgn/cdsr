<div class="section">
<p class="legend">Keynote Lecture</p>
<h6 class="info">9:15 AM - 10:00 AM | Session Chair: Dr. Mojtaba Ahmadi, Carleton University</h6>
<h3 class="title">Robotic Interventions: Achievements, Challenges, and Future Prospects<br>
<small><a href="http://www.ryerson.ca/~fsharifi/" target="_blank">Dr. Farrokh Janabi-Shariﬁ, Ryerson University, Canada</a></small></h3>
<br>

<dl class="dl-horizontal">
  <dt>Abstract</dt>
  <dd>Robotic interventions have recently been proposed to replace manual operations of continuum systems such as catheters and endoscopes. Many of such operations are lengthy and often depend on fluoroscopy (X-ray) for guiding the device. The occupational hazards in medical interventions are serious. The advantages of robotic operations include releasing the interventionists from exposure to hazardous radiation, improving ergonomic factors, integrating the precision of robots into operations, less dependency on the operator’s skills, and possibility for multi-tasking. The primary focus of this talk will be on robotic cardiovascular catheterization in which two or more catheters are operated from a distance. Despite promising aspects of robotic catheterization, many modeling, sensing and control issues remain to be addressed. In addition to the characteristic issues of catheters (such as severe nonlinearities, coupled mechanics, under-actuation, low stiffness and dexterity), their operation in confined spaces also imposes major constraints on sensing and servo feedback.<br>
  This presentation will provide an overview of recent advances on robotic cardiac catheterization. First, non-conventional modeling approaches for catheters will be reviewed. Next, novel sensing and estimation techniques, and servo control structures for semi-autonomous catheterization will be presented. Finally future directions of research will be outlined. The results of this research can potentially be used to enable manipulating soft longitudinal structures in different scales, opening the door to new frontier in many disciplines such as biology, medicine, material science, and manufacturing</dd>
</dl>
